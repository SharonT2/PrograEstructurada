public class figuras {
    public static void main(String[] args) {

        //Cuadrado
        double l = 10;
        //Rectángulo
        double a = 2;
        double b = 5;
        //Triángulo;
        double x = 3;
        double h = 2.5981;

        double areaC = l*l;
        double areaR = b*a;
        double areaT = (0.5*x)*h;

        System.out.println("Area cuadrado: " + areaC + " Area rectangulo: " + areaR + " Area triangulo: " + areaT);

    }
    
}
