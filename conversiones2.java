public class conversiones2 {
    public static void main(String[] args) {
        //Conversión de dólares a pesos
        double cantidad = 10;//cantidad en dólares
        double peso = 18.90;//Pesos en un dolar
        double dolar = 1;
        double resultado = (cantidad * peso)/dolar;
        System.out.println("Cantidad en dolares ingresada: " + cantidad + " Cantidad equivalente en pesos: " + resultado);
    }
}
